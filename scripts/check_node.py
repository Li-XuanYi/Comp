def check_node06() -> List[str]:
    failures = []
    metrics_path = PROJECT_ROOT / "outputs" / "tables" / "node06_baseline_metrics.csv"
    pred_path = PROJECT_ROOT / "outputs" / "tables" / "node06_baseline_validation_predictions.csv"
    for path in [metrics_path, pred_path]:
        if not path.exists():
            failures.append("missing: {}".format(path.relative_to(PROJECT_ROOT)))
    if failures:
        return failures
    metrics = pd.read_csv(metrics_path)
    required_metrics = {"MAE", "RMSE", "SMAPE"}
    missing = required_metrics - set(metrics.columns)
    if missing:
        failures.append("missing metric columns: {}".format(", ".join(sorted(missing))))
    elif metrics[list(required_metrics)].isna().any().any():
        failures.append("baseline metrics contain missing values")
    if len(metrics) < 3:
        failures.append("expected at least three baselines")
    predictions = pd.read_csv(pred_path)
    if predictions.empty:
        failures.append("validation predictions are empty")
    return failures


def check_node07() -> List[str]:
    failures = []
    metrics_path = PROJECT_ROOT / "outputs" / "tables" / "node07_model_metrics.csv"
    pred_path = PROJECT_ROOT / "outputs" / "tables" / "pred_demand_2019_02_01_hourly.csv"
    fig_path = PROJECT_ROOT / "outputs" / "figures" / "model_validation_compare.png"
    for path in [metrics_path, pred_path, fig_path]:
        if not path.exists():
            failures.append("missing: {}".format(path.relative_to(PROJECT_ROOT)))
    if failures:
        return failures
    pred = pd.read_csv(pred_path, parse_dates=["datetime_hour"])
    if pred["predicted_demand"].lt(0).any():
        failures.append("negative demand prediction")
    hours = pred["datetime_hour"].dt.hour.nunique()
    if hours != 24:
        failures.append("prediction does not cover 24 hours")
    expected_rows = pred["zone_id"].nunique() * 24
    if len(pred) != expected_rows:
        failures.append("prediction row count mismatch")
    if pred[["zone_id", "datetime_hour", "predicted_demand"]].isna().any().any():
        failures.append("prediction has key missing values")
    if fig_path.stat().st_size <= 0:
        failures.append("model validation figure is empty")
    return failures


def check_node08() -> List[str]:
    failures = []
    ref_path = PROJECT_ROOT / "outputs" / "tables" / "od_distance_duration_reference.csv"
    fhv_path = PROJECT_ROOT / "outputs" / "tables" / "fhv_with_estimated_distance.csv"
    for path in [ref_path, fhv_path]:
        if not path.exists():
            failures.append("missing: {}".format(path.relative_to(PROJECT_ROOT)))
    if failures:
        return failures
    ref = pd.read_csv(ref_path)
    fhv = pd.read_csv(fhv_path)
    if ref.empty or fhv.empty:
        failures.append("node08 output is empty")
    if fhv["estimated_distance"].isna().any():
        failures.append("FHV estimated_distance has missing values")
    if not fhv["estimated_distance"].gt(0).all():
        failures.append("FHV estimated_distance must be positive")
    if fhv["estimated_od_duration_min"].isna().any():
        failures.append("FHV estimated_od_duration_min has missing values")
    clean_path = PROJECT_ROOT / "data" / "interim" / "fhv_clean.parquet"
    if clean_path.exists() and len(fhv) != len(pd.read_parquet(clean_path)):
        failures.append("FHV enriched row count does not match clean input")
    return failures


def check_node09() -> List[str]:
    failures = []
    result_path = PROJECT_ROOT / "outputs" / "tables" / "fhv_pricing_results.csv"
    summary_path = PROJECT_ROOT / "outputs" / "tables" / "node09_pricing_model_summary.csv"
    for path in [result_path, summary_path]:
        if not path.exists():
            failures.append("missing: {}".format(path.relative_to(PROJECT_ROOT)))
    if failures:
        return failures
    result = pd.read_csv(result_path)
    required = {"fhv_price", "taxi_reference_price", "estimated_cost", "estimated_profit"}
    missing = required - set(result.columns)
    if missing:
        failures.append("missing pricing columns: {}".format(", ".join(sorted(missing))))
        return failures
    if result[list(required)].isna().any().any():
        failures.append("pricing result has key missing values")
    if not result["fhv_price"].gt(0).all():
        failures.append("fhv_price must be positive")
    fhv_distance = PROJECT_ROOT / "outputs" / "tables" / "fhv_with_estimated_distance.csv"
    if fhv_distance.exists() and len(result) != len(pd.read_csv(fhv_distance)):
        failures.append("pricing row count does not match FHV input")
    return failures


CHECKS: Dict[str, Callable[[], List[str]]] = {
    "node00": check_node00,
    "node01": check_node01,
    "node02": check_node02,
    "node03": check_node03,
    "node04": check_node04,
    "node05": check_node05,
    "node06": check_node06,
    "node07": check_node07,
    "node08": check_node08,
    "node09": check_node09,
}


def run_check(node: str) -> int:
    if node == "all":
        nodes = sorted(CHECKS)
    else:
        nodes = [node]

    all_failures = []
    for current_node in nodes:
        check = CHECKS.get(current_node)
        failures = check() if check else check_not_implemented(current_node)
        if failures:
            all_failures.append((current_node, failures))
        else:
            print("[PASS] {}".format(current_node))

    if all_failures:
        for failed_node, failures in all_failures:
            print("[FAIL] {}".format(failed_node))
            for failure in failures:
                print("  - {}".format(failure))
        return 1

    return 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Validate node outputs.")
    parser.add_argument("--node", required=True, help="Node id, for example node00.")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    return run_check(args.node.lower())


if __name__ == "__main__":
    raise SystemExit(main())
