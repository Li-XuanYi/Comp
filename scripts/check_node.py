def check_not_implemented(node: str) -> List[str]:
    return [
        "{} is not implemented yet. Run nodes sequentially and complete earlier nodes first.".format(
            node
        )
    ]


def check_node01() -> List[str]:
    """Check the raw data audit table generated by Node 01."""
    failures = []
    audit_path = PROJECT_ROOT / "outputs" / "tables" / "node01_data_audit.csv"
    if not audit_path.exists():
        return ["missing: outputs/tables/node01_data_audit.csv"]

    try:
        audit = pd.read_csv(audit_path)
    except Exception as exc:
        return ["could not read audit csv: {}".format(exc)]

    required_columns = [
        "dataset",
        "n_rows",
        "n_cols",
        "columns",
        "missing_rate_summary",
        "datetime_min",
        "datetime_max",
    ]
    missing_columns = [column for column in required_columns if column not in audit.columns]
    if missing_columns:
        failures.append("missing audit columns: {}".format(", ".join(missing_columns)))
        return failures

    required_datasets = {
        "yellow",
        "green",
        "fhv",
        "taxi_zone_lookup",
        "taxi_zones_shp",
    }
    found = set(audit["dataset"].astype(str))
    missing_datasets = required_datasets - found
    if missing_datasets:
        failures.append("missing datasets: {}".format(", ".join(sorted(missing_datasets))))

    for dataset in sorted(required_datasets & found):
        row = audit.loc[audit["dataset"].astype(str) == dataset].iloc[0]
        if int(row["n_rows"]) <= 0:
            failures.append("{} has no rows".format(dataset))
        if int(row["n_cols"]) <= 0:
            failures.append("{} has no columns".format(dataset))
        if not str(row["columns"]).strip():
            failures.append("{} has empty column listing".format(dataset))

    trip_datasets = {"yellow", "green", "fhv"}
    for dataset in sorted(trip_datasets & found):
        row = audit.loc[audit["dataset"].astype(str) == dataset].iloc[0]
        if not str(row["datetime_min"]).strip() or not str(row["datetime_max"]).strip():
            failures.append("{} missing datetime bounds".format(dataset))

    return failures


def check_node02() -> List[str]:
    failures = []
    report_path = PROJECT_ROOT / "outputs" / "tables" / "node02_cleaning_report.csv"
    if not report_path.exists():
        failures.append("missing: outputs/tables/node02_cleaning_report.csv")

    clean_paths = {
        "yellow": PROJECT_ROOT / "data" / "interim" / "yellow_clean.parquet",
        "green": PROJECT_ROOT / "data" / "interim" / "green_clean.parquet",
        "fhv": PROJECT_ROOT / "data" / "interim" / "fhv_clean.parquet",
    }
    for dataset, path in clean_paths.items():
        if not path.exists():
            failures.append("missing: {}".format(path.relative_to(PROJECT_ROOT)))
            continue
        try:
            df = pd.read_parquet(path)
        except Exception as exc:
            failures.append("{} read failed: {}".format(dataset, exc))
            continue
        if df.empty:
            failures.append("{} clean data is empty".format(dataset))
            continue
        if not df["duration_min"].gt(0).all():
            failures.append("{} has non-positive duration".format(dataset))
        if not df["duration_min"].le(180).all():
            failures.append("{} has duration over 180 minutes".format(dataset))
        if not pd.to_datetime(df["pickup_datetime"]).ge("2019-01-01").all():
            failures.append("{} has pickup before 2019-01-01".format(dataset))
        if not pd.to_datetime(df["pickup_datetime"]).lt("2019-02-01").all():
            failures.append("{} has pickup on/after 2019-02-01".format(dataset))
        if dataset in {"yellow", "green"}:
            if not df["trip_distance"].gt(0).all():
                failures.append("{} has non-positive trip distance".format(dataset))
            if not df["total_amount"].gt(0).all():
                failures.append("{} has non-positive total amount".format(dataset))
            if not df["fare_amount"].gt(0).all():
                failures.append("{} has non-positive fare amount".format(dataset))

    return failures


def check_node03() -> List[str]:
    failures = []
    panel_path = PROJECT_ROOT / "data" / "processed" / "hourly_demand_panel.parquet"
    summary_path = PROJECT_ROOT / "outputs" / "tables" / "node03_demand_panel_summary.csv"
    if not panel_path.exists():
        failures.append("missing: data/processed/hourly_demand_panel.parquet")
        return failures
    if not summary_path.exists():
        failures.append("missing: outputs/tables/node03_demand_panel_summary.csv")

    panel = pd.read_parquet(panel_path)
    required = {
        "zone_id",
        "datetime_hour",
        "date",
        "hour",
        "weekday",
        "is_weekend",
        "pickup_count",
        "yellow_count",
        "green_count",
    }
    missing = required - set(panel.columns)
    if missing:
        failures.append("missing panel columns: {}".format(", ".join(sorted(missing))))
        return failures
    if panel["pickup_count"].lt(0).any():
        failures.append("pickup_count has negative values")
    if panel["datetime_hour"].min() != pd.Timestamp("2019-01-01 00:00:00"):
        failures.append("wrong panel start")
    if panel["datetime_hour"].max() != pd.Timestamp("2019-01-31 23:00:00"):
        failures.append("wrong panel end")
    expected_rows = panel["zone_id"].nunique() * 24 * 31
    if len(panel) != expected_rows:
        failures.append("panel is not complete: got {}, expected {}".format(len(panel), expected_rows))
    duplicate_count = panel.duplicated(["zone_id", "datetime_hour"]).sum()
    if duplicate_count:
        failures.append("duplicate zone-hour rows: {}".format(int(duplicate_count)))
    return failures


def check_node04() -> List[str]:
    failures = []
    feature_path = PROJECT_ROOT / "data" / "processed" / "hourly_features.parquet"
    summary_path = PROJECT_ROOT / "outputs" / "tables" / "node04_feature_summary.csv"
    weather_path = PROJECT_ROOT / "data" / "external" / "weather_hourly.csv"
    for path in [feature_path, summary_path, weather_path]:
        if not path.exists():
            failures.append("missing: {}".format(path.relative_to(PROJECT_ROOT)))
    if failures:
        return failures
    features = pd.read_parquet(feature_path)
    required = {
        "temperature",
        "precipitation",
        "snow_indicator",
        "wind_speed",
        "visibility",
        "is_weekend",
        "is_holiday",
        "is_morning_peak",
        "is_evening_peak",
        "is_night",
        "hour_sin",
        "hour_cos",
        "weekday",
        "month",
        "day",
    }
    missing = required - set(features.columns)
    if missing:
        failures.append("missing feature columns: {}".format(", ".join(sorted(missing))))
    key_missing = features[list(required)].isna().mean().max()
    if key_missing > 0:
        failures.append("key feature missing rate is {}".format(key_missing))
    return failures


